const { Schema, model } = require('mongoose');
const thoughtSchema = require('./Thought');

const validateEmail = (email) => {
  const re = /^\w+([\.-]?\w+)*@\w+([\.-]?\w+)*(\.\w{2,3})+$/;
  return re.test(email);
};

